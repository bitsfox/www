        <p> 对熟悉 Windows 系列系统平台的用户，有套软件倒是在开发 PHP 程序上是蛮方便的，就是 PHP Editor，若对这软件有兴趣，不妨到 
          <a href="/to.asp?url=http://www.soysal.com/PHPEd" tppabs="/to.asp?url=http://www.soysal.com/PHPEd">http://www.soysal.com/PHPEd</a> 
          下载最新的版本。同时还需要下载 PHP 程序 Win32 的版本。安装好 PHP Win32 版后，在 PHP Editor 中配置好 
          PHP Win32 的路径，就可以让您轻松地开发 PHP 程序了。至于在写作 PHP 程序的工作平台上，有没有 Web 服务器倒是不重要，因为在 
          PHP Editor 的环境中写好的程序，按下执行的键后，由 PHP Editor 直接将所写的 PHP 程序送给 PHP Win32，并将执行结果处理后，呈现在用户的面前。
        <p> 若您和笔者一样，对 Linux/UNIX 很偏执，或许可以尝试使用 <a href="http://bluefish.linuxbox.com/" tppabs="/to.asp?url=http://bluefish.linuxbox.com/">Bluefish</a>: 
          蓝鱼 PHP 写作工具，只要装好 Linux 及 X-Window 之后就可以使用了。要使用中文的话，需要在 X-Window 加入中文的字型及输入法，要方便的话，使用 
          RedHat 加上 CLE 中文支持就好了。 
        <p> 假使您已是老 Linux/UNIX 了，也不喜欢使用这些专门的开发工具。相信您对 vi 或是 <a href="/to.asp?url=http://www.gnu.org/software/emacs/emacs.html" tppabs="/to.asp?url=http://www.gnu.org/software/emacs/emacs.html">Emacs</a> 
          一定相当熟悉，那么就在装好 Web 服务器和 PHP 程序后，直接用 vi 或 Emacs 写 PHP 程序就可以了，并且可以直接看到执行的真正结果，和数据库或是其它的服务程序链接，也不会有什么问题。这种「所写即所得」的方式，岂不痛快!! 
