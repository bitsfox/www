除了上述的运算符号之外，还有一些运算符号难以归类。<p>

<table border=1>
<tr><th>符号</th><th>意义</th></tr>
<tr><td>$</td><td>变量</td></tr>
<tr><td>&amp;</td><td>变量的地址 (加在变量前)</td></tr>
<tr><td>@</td><td>不显示错误信息 (加在函数前)</td></tr>
<tr><td>-&gt;</td><td>类的方法或者属性</td></tr>
<tr><td>=&gt;</td><td>数组的元素值</td></tr>
<tr><td>?&nbsp;:</td><td>三元运算子</td></tr>
</table><p>

其中比较特殊的是三元运算子 ? &nbsp; :，以下例来解释<p>

<code>(expr1) ? (expr2) : (expr3);</code><p>

若 expr1 的运算结果为 true，则执行 expr2；否则执行 expr3。实际上它有点类以 if...else 循环，但可以让程序较精简有效率。

