<li>qmail 与 vmailmgr 系统
<li>压缩文件处理
<li>XML 解析  
</ul><p>

除此之外，一般语言有的数学运算、时间处理、文件系统、字符串处理、行程处理等功能，它一样都不缺。再加上它是免费的系统，使得成本与效益比，几乎等于无限大！

