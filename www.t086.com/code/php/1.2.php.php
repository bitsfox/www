PHP 在数据库方面的丰富支持，也是它迅速走红的原因之一，它支持下列的数据库或是资料表：<p>
<ul type=disc>
<li>Adabas D
<li>DBA
<li>dBase
<li>dbm
<li>filePro
<li>Informix
