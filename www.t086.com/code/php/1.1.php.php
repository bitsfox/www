PHP 最初是在公元 1994 年 Rasmus Lerdorf 开始计划发展。在 1995 年以 Personal Home Page Tools (PHP Tools) 开始对外发表第一个版本。在这早期的版本中，提供了访客留言本、访客计数器等简单的功能。随后在新的成员加入开发行列之后，在 1995 年中，第二版的 PHP 问市。第二版定名为 PHP/FI(Form Interpreter)。PHP/FI 并加入了 mSQL 的支持，自此奠定了 PHP 在动态网页开发上的影响力。在 1996 年底，有一万五千个 Web 网站使用 PHP/FI；在 1997 年中，使用 PHP/FI 的 Web 网站成长到超过五万个。而在 1997 年中，开始了第三版的开发计划，开发小组加入了 Zeev Suraski 及 Andi Gutmans，而第三版就定名为 PHP3。<p>

PHP3 跟 Apache 服务器紧密结合的特性；加上它不断的更新及加入新的功能；并且它几乎支持所有主流与非主流数据库；再以它能高速的执行效率，使得 PHP 在 1999 年中的使用网站超过了十五万!!它的源代码完全公开，在 Open Source 意识抬头的今天，它更是这方面的中流砥柱。不断地有新的函数库加入，以及不停地更新的活力，使得 PHP 无论在 UNIX 或是 Win32 的平台上都可以有更多新的功能。它提供丰富的函数，使得在程序设计方面有着更好的支持。<p>

PHP 的第四代 Zend 核心引擎已经进入测试阶段。整个脚本程序的核心大幅改动，让程序的执行速度，满足更快的要求。在最佳化之后的效率，已较传统 CGI 或者 ASP 等程序有更好的表现。而且还有更强的新功能、更丰富的函数库。无论您接不接受，PHP 都将在 Web CGI 的领域上，掀起巅覆性的革命。对于一位专职 Web Master 而言，它将也是必修课程之一。

