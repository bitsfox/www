讲到 PHP 的全名就蛮有趣的，它是一个递归的缩写名称，"PHP: Hypertext Preprocessor"，打开缩写还是缩写。PHP 是一种 HTML 内嵌式的语言 (类似 IIS 上的 ASP)。而 PHP 独特的语法混合了 C、Java、Perl 以及 PHP 式的新语法。它可以比 CGI 或者 Perl 更快速的执行动态网页。<p>

