在安装 PHP 做为 WWW 服务器的一部份时，我们可以考虑用 UNIX 操作系统；或者是 Windows NT/95 等 Win32 API 的平台。当然，大部份的人都会使用 UNIX 来当作 PHP 的执行平台 (在 Windows NT 的用户大多数都会选择 IIS + ASP)，因此，本书的所有内容以及范例程序都是在 UNIX 上为主。实际上，Linux + Apache + PHP 应是最经济的选择，因为这样的组合几乎是不用钱的，成本与效益比这也是最好的选择。而许多成功网站的经验，更是采用这种组合最好的佐证。
        <p> Linux 操作系统方面，您可以选择各式的 Linux 套件，包括 <a href="/to.asp?url=http://www.slackware.com/" tppabs="/to.asp?url=http://www.slackware.com/">Slackware 
          Linux</a>、<a href="/to.asp?url=http://www.redhat.com/" tppabs="/to.asp?url=http://www.redhat.com/">RedHat</a>、<a href="/to.asp?url=http://www.calderasystems.com/" tppabs="/to.asp?url=http://www.calderasystems.com/">OpenLinux</a>、<a href="/to.asp?url=http://www.suse.com/" tppabs="/to.asp?url=http://www.suse.com/">SuSE</a>....等等，反正这方面的软件在店里也是很容易而且很便宜就可以买到。对学生而言，也可以去各大 
          FTP 站下载完整的系统安装。
        <p> Apache 服务器则是目前最多 WWW 网站所采用的服务器。您可以到 <a href="/to.asp?url=http://www.apache.org/" tppabs="/to.asp?url=http://www.apache.org/">http://www.apache.org</a> 
          下载最新版的程序及相关文件，若您觉得从国外下载要很久的话，也可以用它的 Mirror 网站下载。
        <p> PHP 则可以去它的官方网站 <a href="/to.asp?url=http://www.php.net/" tppabs="/to.asp?url=http://www.php.net/">http://www.php.net</a> 
          下载所需要的程序。
        <p>

虽然目前 WindowsNT 或者 Windows98 等 Win32 的系统平台也能安装 PHP 及 Apache 服务器，不过这似乎没什么道理，因为 PHP 和 Apache 在 UNIX下可以跑得更快更好。
        <p> 当然，若想使用商业化的系统平台，SUN、IBM、HP、DEC、SGI、NEC 等公司都提供相关的 UNIX 或者是 WindowsNT 
          的系统平台。加上高安全性调整过后的 Apache 服务器：Stronghold 
          或是其它支持 SSL 的 Apache 版本。这种组合，相信能满足商业化的需求。而 PHP 就扮演着快速方便的 CGI 角色，让客户对网站的服务品质更加满意。 
