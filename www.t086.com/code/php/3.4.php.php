由于 PHP 的大部份语法都是继承了 C 语言的语法，因此，在流程控制方面，也是有着和 C 语言极类似的循环。PHP 的循环不像 ASP 般可以使用 goto 的 BASIC 语法，PHP 是结构化的程序语言，流程的设计上有一定的规定，而不能用 BASIC 的概念来乱跳到别的部分中。<p>

PHP 的语法没有像 C 语言的 main(){} 部分，其实整个 PHP 主页面 (就是浏览器输入的 URL) 就是 main(){} 部分，这点和其它的解析程序，如 Prel、Python、Shell Script 倒是很像。<p>

在流程的部分分隔符号上，都是使用 { 当作部分的开头，用 } 当作结尾，和 C 语言相同。不过 C 可以定义 begin 当开头、end 当结尾 (像 Pascal)，而 PHP 中不能做这种特殊的定义。<p>

而 PHP 语法中在每条指令结束时都要加上分号 ;，但是在部分结尾符号 } 后面不用加上分号结束。

