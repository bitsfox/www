<div align=right><a href="group.php-40.php" alt="group.php?40">Perl 相容语法函数库</a></div><br><h1><font color=0000bb>preg_match</font></h1><p>
字符串比对解析。<p>
<font color=ff8000>语法:</font> <b>int preg_match(string pattern, string subject, array [matches]);</b><p>
<font color=ff8000>返回值:</font> 整数/数组<p>
<font color=ff8000>函数种类:</font> 资料处理<p>
<p><table border=0 cellspacing=0 cellpadding=0 width=100% height=1><tr><td height=1 bgcolor=c0c0c0><table border=0 cellspacing=0 cellpadding=0><tr><td></td></tr></table></td></tr>
<tr><td align=left><font color=ff8000>内容说明</font></td></tr></table><p>本函数以 pattern 的规则来解析比对字符串 subject。比对结果返回的值放在数组参数 matches 之中，matches[0] 内容就是原字符串 subject、matches[1] 为第一个合乎规则的字符串、matches[2] 就是第二个合乎规则的字符串，余类推。若省略参数 matches，则只是单纯地比对，找到则返回值为 true。<p>
